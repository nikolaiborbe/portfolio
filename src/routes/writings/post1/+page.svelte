<br />

<p>
	After a good hour of work, they met back up. Mykola, after having dug down all their ration,
	packed the computer and all the electronics, and disassembled and hid the shelter, was excused.
	The same was Boh, after having hurried his way around a 3 kilometers radius, activating all their
	hidden alarms. Now they felt relatively safe at least. “All done!” Boh cried out to Mykola in a
	distance. “Same!” he quickly responded, both meeting up at the camp. “When do you think they’ll be
	here?” Boh asked. “Could be at any time, remember, the alarms don’t have to be activated for them
	to have passed through. They can malfunction, we can’t be sure they are not already here. We need
	to be on high alert. I’ll make us some lunch, head to the hill and bring the radio, I’ll bring the
	food. And for god’s sake. Stay quiet.” Mykola cried. Boh did as commanded and quickly grabbed a
	radio, he shucked the other one to Mykola, and was already a good way up the hill before it got
	caught. Using the small trees, and stone cracks to drag himself up, Boh quickly found his way to
	the top. They had dug a little divot that gave them a good overlook of the forest surrounding
	them. The hideout was specifically made for this situation: see the enemy, report, and hide. In
	the divot they had water, food, and a couple of spare batteries, enough to let them stay there for
	3 days straight. They had no guns other than a pistol each; the idea was that if they got caught,
	they would be dead either way, and not having a gun would incentivize hiding; Ukraine also had a
	shortage of guns, and the few they had where needed elsewhere. Boh laid himself down onto his
	black and brown sheepskin that was gifted to him by another civilian. The man told him he had left
	his old job as a sheep farmer to help out the resistance. Giving all the sheepskins away to anyone
	that might need them, probably expecting not to return (show don’t tell?). Boh had only been up on
	the hill once before when he was helping Mykola dig. They hadn’t brought a lot of tools, so they
	both used a simple pocketknife and their hands for the job. Looking out over the quiet forest Boh
	felt his blood cool. “Mykola, I’m all set up on Asgard, over.” Boh called over the radio. “I can
	hear you clearly, over and out.” Mykola responded after a while, “the food will be done shortly.”
	15 minutes pasted before Mykola could be heard making his way up the hill, taking the easier, but
	longer route. “Here’s your stew. It might be your last for a while, so enjoy it dearly.” “Thanks!”
	Boh responded, reaching out and grabbing the mix. “Now we’ll wait.”
</p>

<br />
