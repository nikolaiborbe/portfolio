<h2 class="text-2xl">A Day</h2>

<p>
	Two long hours went by, and slowly they started feeling sick with apprehension. They could hear
	sounds of wind, and all the sounds the wind would create, even the imaginary ones. The only thing
	keeping them from growing insane was the time still ticking forward. In a normal situation they
	would both be on the brink of a deep and slumber sleep, but the tension rising made sure that
	would not happen. The sheepskin was slowly growing more and more uncomfortable as the lads
	continuously kept shifting around. A bird (tweak?) could be heard every now and then, and the echo
	from a cat owl was constantly felt through the forest. “Did you hear that,” Mykola whispered.
	“Hear what?” “The branch crying!” “No?” said Boh, as quietly as he could manage. “There it was
	again, did you...” “Yeah, shut up!” Boh quickly responded. They were lying dead still for what
	felt like an eternity. Then it happened again, and again. Footsteps where quietly getting closer.
	Suddenly Boh saw the outline of a small figure ever getting closer. “Over there,” Boh nodded.
	Mykola had also heard it and was already staring straight at what looked like a man coming closer.
	“It’s only one.” Boh whispered. “I can see it,” Mykola agreed “just be quiet and keep your head
	down.” The man was slowly getting closer and was slowly headed towards their camp. “What the fuck
	do we do!” Boh panicked. “Just stay down, don’t make any fucking noise!” Mykola whispered angerly.
	“What if he finds our stuff?” “He won’t.” “But…” Boh started. “If he does, we will take him
	hostage. Simple as that, but he won’t.” Mykola interrupted. The man, only a few meters away now
	suddenly stopped where they could see him clearly. The man wasn’t a man, he was a boy. Probably no
	more than 18 years, maybe even younger. He was wearing a Russian, over-sized, military uniform.
	His face, even though it was very dirty, was spotless, unscared, unshaped. He was clearly alone
	because he was scared. Over his right shoulder he was carrying a military assault rifle (name). He
	had taken off his (west) and was not carrying any visible ammunition or explosives. On his back he
	was carrying a black, and dark blue, backpack, not of the military kind, this one he had probably
	found on his adventure. The boy was looking around, it seemed like he had spotted something, only
	standing a couple of meters away from their hidden rations. The snow looked different in this
	area, almost like it had been used. The boy suddenly and quickly turned his head around, then
	back, when he looked back, he had a realization, slowly looking to the snow then lifting his head
	slowly, searching the hillside, slowly rising, until he was staring them both in the eyes. It took
	a couple of seconds before anyone reacted, but all at the same time panicked and reach for their
	guns. The kid was quickest and just after a seconds started firing; holding down his trigger
	bullets went flying all around them, many hitting the hill right under, many the trees behind
	them, but no bullet got close. No more than 15 bullets were fired before the gun stopped, it
	wasn’t responding to the trigger. He tried again and again but he knew he was out. Mykola and Boh
	quickly realized and were down the hill and was holding him at gunpoint before he could realize
	what had happened. “Don’t Fucking Move a Finger!” Mykola yelled. Now standing only two arm
	distances away. “I will shoot if you test me,” Mykola cried, a lot quieter this time. The kid was
	scared out of his mind, arms and legs shaking, a few tears rolling downs his cheekbones. You could
	see a little wet spot on his crotch slowly moving down his left inner pant leg. “Please don’t
	kill. Please,” the kid begged slowly bending his knees and putting his head down, “please.” “Are
	you Russian?” Mykola asked in an almost whispering voice, scared of being heard, but still wanting
	to make sure the kid heard him. “Yes, yes I am. My name is Nikolai, I ran away from my unit, I
	want no harm. Please don’t kill me.” “What the fuck do we do now Mykola?” Boh whispered
	aggressively. It took some time before Mykola responded. “You remember when we saw Lone Survivors
	in the cinema, and what happened to the Matt Damon guy.” “Are you saying we should kill him?” “No,
	no, my point was only that we can’t trust he won’t run back, so we need to think of something
	else.” “We can use the ropes from the tents to tie him up. Then we take the moss under the snow
	and a branch and stick it in his mouth to keep him quiet. You can take the spare fir branches and
	hide him next to the boulder side. We will give him some water and food once a day, and then take
	him with us when we leave for Kaniv.” “You do know there can be more Russians, right? We should
	just strangle and hide him. It’s too risky not to.” “You don’t want to kill someone; he will haunt
	your memory till the day you die. You will not be able to live a happy man again Boh. Trust me.”
	“At least I will have a life.” Boh angerly responded. “The grief I have suffered makes me question
	the value of life, I see his innocent face every day when I go to bed, and he was holding me in a
	stalemate. This one can’t do us anything.” Mykola said frustrated. “Have it your way, but we
	should still question him.” “Yeah, yeah, of course.” The kid had calmed down after the reassurance
	he might not die.
</p>

<br />

<p>
	"Are the Russians headed this way?" Said Mykola. "I, I don't know. As I said I, ran away from my
	unit right after they captured Kiev, about 24 hours ago." Nikolai responded calmly. "I found this
	backpack on my way here. Stuffed it with food I stole from an elderly woman in an old small red
	house about 5 kilometers that way," he said pointing in the direction he came from. "My plan was
	to try to get into Poland with the civilians and hide out the war," he continued. "If you let me
	go I'll be on my way and you will never see me again." Boh looked to Mykola who had a concerned
	look fixed to his face. "He could be telling the truth," said Boh. "I know, but he could not be.
	Let's tie him up, and when we leave in tree days we let him go. If he is telling the truth he
	should still be able to get to Poland safely." Mykola concluded. Boh made his way towards their
	dug down tent. They had no use for it anymore seeing that comfort was far too risky. He brushed
	away the snow until he found the outer layer of their old green tent. He dragged it out, letting
	the snow fall from the side until he could see a thin yellow line appear. Following that, he
	grabbed his small pocket knife–he had kept it from his days as a sailor and liked to show it off.
	The knife was a flip knife, with a worn out shaft. On the shaft was engraved 63, his own number.
	He grabbed the line, putting his knife into a little loop he made with the rope and cut it off. He
	did this to all the lines creating seven or so small segments of the yellow line. Continuing, he
	grabbed the lines and tied them together using sheet bends––a knot his old captain thought him.
	Having now made a long line he took a good hold of the thin top layer of the tent and dragged it
	out. Using his knife, he cut up squares which he thought might be found useful as a (mouth cover
	to keep Nikolai quiet). Once he was done he took the scrap material and hid it under a new layer
	of wet snow. It was a good thing they were leaving in three days since the snow was about to melt
	leaving all their stuff out in the open. Boh made his way back to Mykola who was still holding
	Nikolai at gun point. "Here, I have made a line and some material I thought we could use as a
	(mouth cover to keep Nikolai quiet)," Boh called out. The lads took a hold of Nikolai, first
	taking the rope around his hands, then tightening it by going around the rope. This created a
	really solid, and almost too tight hold around his hands. Once done they took the rest of the rope
	and continued the pattern around his leg before they finished by lashing his hands and feet close
	together forcing Nikolai into a strange, bent back, yoga position. After, they curled up the tent
	material Boh had made into a thick roll and forced it tight into his mouth and around his head
	creating a bit. Finishing up, Boh went around gathering fir branches which they used to hide
	Nikolai body. "We will come back checking on you every now and then. If you want to live, don't
	move and stay quiet," Mykola whispered aggressively in a threatening manner. Boh and Mykola made
	their way back up the hill. After reaching the summit Mykola turned to Boh. "We did the right
	thing." (Note: make sure fir trees grow in Ukrain.)
</p>

