<h2 class="text-2xl">A Night</h2>

<p>
	Nikolai let a few moments pass after Boh had left. He knew that this was his best opportunity to
	escape, especially now that the ropes where to loose for their intended purpose. Nikolai had grown
	a liking to the lads and didn't want to inflict them any pain; he simply planned to steal Mykola's
	gun, grab a bit of food, and escape. He waited just a couple of seconds before he started twisting
	his wrists slowly, turning them from side to side, ever loosening the rope. After just a couple of
	minutes he knew they where loose enough to let his hands escape. He pulled gently, no good. He
	tried again, now with explosive force; the rope let his hands go from their tight grip. Right away
	Nikolai launched for the pistol on Mykola's side. He managed to grab it quickly before Mykola
	realized. "Don't move!" Nikolai said with a scared tone in his voice. He hadn't planned this exact
	moment and understood that this next part was going to be tricky. "Just let me go and I won't hurt
	you." "Ok!" Mykola replied. Nikolai had to act quickly since Boh could come back at any time. He
	used the hand he was holding the gun with–it was still numb after the tight ropes–and managed to
	pull off the remaining rope. "Just stay here and I'll be on my way," Nikolai said while he raised
	his head, having looked to his hands so he could more easily untie the rope. He now looked
	straight into the barrel of Mykola's gun, pointing straight towards his head. Nikolai realized he
	hadn't grabbed Mykolas gun, rather, Boh had forgot to bring his own and he was now holding his
	gun. In a panic Nikolai pressed the trigger on his gun. Suddenly, a bullet jumped out of his gun,
	flying with incredible speed towards Mykola. Before he had realized what he had done Mykola's face
	was pushed back, and as his body followed, he landed face first on the snow right outside of the
	divot. Filled with shock, Nikolai felled back. He didn't quite understand what he had just done;
	he had never killed anyone before. He felt a certain emptiness and loneliness, but his
	survival-instincts quickly kicked in and he was well on his was before the blood-river from the
	cadaver had reached the bottom of the hillside. Never to be seen again.
</p>
