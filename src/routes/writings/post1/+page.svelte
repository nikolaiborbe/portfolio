<br />

<p>
	Two hours passed with little to no talking. Just a simple comment every now and then. Both where
	daydreaming to themselves and had little interest in talking. Suddenly they heard a quiet beeping
	noise coming from the computer. Boh instinctively raised his head, looking straight into Mykolas
	eyes, then quickly at the computer. He quickly reached for the computer and opened it, vigorously
	scanning for information. “The North-east alarm in sector four is triggered,” he said. “Fuck!”
	“I’m scared Mykola. I’m scared” “Stay calm. As long as we don’t get seen, we should be good.” The
	sun was rising more and more and the temperature with it. It was still late winter, so icy snow
	was covering the moss and humus hiding underneath.
</p>

