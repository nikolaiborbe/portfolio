<p>
	The weather had turned dark, and rain clouds could be spotted through the cracks in the roof of
	the forrest. Out of no where they hear Nikolai making his best effort to call out for help. He
	quickly found out it was difficult to make any audible words through the (mouth cover to keep
	Nikolai quiet) but never the less Boh and Mykola had heard him. Boh made his way down the hill
	using the stems of the small trees as support. "Shut it." Boh said aggressively. Nikolai in
	response continued to make the same inaudible noise as before. Boh saw no other solution and
	decided to gently remove the (mouth cover to keep Nikolai quiet) from Nikolai's mouth. "My hands,"
	said Nikolai, who had lost all life from his voice. Boh rolled Nikolai's limp body over, letting
	it fall down on its side after reaching the tipping point. He saw right away that something was
	wrong. His bare hands were blue; his fingers were dark probably from a combination of the cold and
	the tight ropes. "Please loosen the ropes, just a bit. I can't feel my hands any longer." Boh
	quickly realized that he couldn't let Nikolai stay like this and decided to loosen the ropes.
	"This won't work," Boh decided, loosening the ropes on his foot as well. "You won't run away will
	you?" "No, no, not at all." Boh didn't really care about Nikolai's response, feeling bad for what
	they had done to him he was confident, or maybe regretfully hopeful, he wouldn't try anything. The
	only rope now tied to Nikolai's body was a symbolic loose rope around his arms. "Come with me,"
	Boh decided leading the way up the hill. After reaching the summit, and turning away Mykola's
	questioning face, Boh told Nikolai to lay down besides them. "He would have died if we let him
	stay down there. It's too hot for the snow, but too cold for the body. He would be laying dead in
	a frozen layer of ice by nightfall." Mykola seemed to agree, once scared for his life, now used to
	the feeling, it wasn't such a big deal any longer. Boh laid himself down onto the sheepskin,
	keeping an eye on Nikolai. He was only laying a meter to his right, and could easily be spotted
	from the ground.The boys had gotten used to each other, and not after long they where talking in a
	lively, but still very quiet, manner. "Where are you from Nikolai?" Mykola asked gently. "Moscow,
	I'm from Moscow, in a small block in the Northern part of the city." Nikolai responded. "I grew up
	there, and was still living there with my mom and dad and two brothers before going into this
	meaningless war." "How old are you kid?" Boh said. "Kid? It's not like you are much older," he
	marked, "I turned 18 this January." "How old are your brothers?" "One as old as you I would
	imagine, he's 20. One younger, he's 15. My older brother was recruited at the same time as me, I
	think he is stationed in Kharkiv. They didn't want us to be together," said Nikolai with a sad
	tone in his voice. "I haven't seen him or my family in 5 months." "I am sorry to hear," said Boh,
	turning his head over and checking on Nikolai's hands. They were less blue, and had finally
	started to get some color back into them. It had been many hours since last time they ate, so Boh
	decided to go down the hill to boil some snow into water for a stew. He dug where he knew the
	ration was hidden, soon finding the plastic box they kept their food in. He opened it, digging
	through the bags until he found himself three packets off stew. He dug the box back down into the
	hole, and then went onto looking for dead fir-tree branches, the type that is excellent for
	starting a fire with. He had to walk 500 meters away from the camp until he found something
	promising. The tree was tall, but manageable. Using his saw, he found a good spot and started
	pushing it to and fro. With every push digging deeper into the stem of the tree. After a good
	minute he started getting through and the tree was slowly leaning further and further, suffering
	more and more, wanting to lay down dead. Finally the tree had had enough, and decided to fall,
	making a large cracking noise while tipping and a vey loud bang that echoed through the forrest
	when it landed. Boh noticed the strangeness to the bang and quickly decided that something was
	off. He quickly started running for the camp, realizing what might have happened. With every step
	he was growing more and more worried. Running faster and faster he was feeling his stomach aching
	for food. He hadn't eaten for almost nine hours and was starting to feel his legs and body
	fatigue. Hoping for the best, he decided to take a quick brake to catch his breath. All of a
	sudden he heard chattering off in a distance, but not from the camp, and not any voices he
	recognized. There were many voices, and all coming from slightly different directions. Fearing the
	worst, Boh went to grab his gun, but vigorously looking for it he didn't find it; he had left his
	pistol at the camp. How stupid of him he thought to himself, but decided to take a closer look.
	Slowly moving towards the camp. He felt as if he was suddenly filled with a strong courage that he
	had never felt before. With every step he was slowly getting closer; his foot was making cracking
	noises in the snow, he moved from tree to tree, using them as cover. "What should we do with this
	guy?" Boh could hear in a distance. Seeing the outline of many people, he counted 11. It looked
	like they had spotted their lookout point, and on it was laying a body. He could see it on the
	jacket, and it was like he had feared, Mykolas dead body. The corpse was laying on its stomach
	with the face planted firmly in the ground. The back of his head was open, letting the wind hit
	directly into an open wound as large as a tennis ball. There was blood covering the snow all
	around him, and down, down, flowing down the hill like a river on a mountainside. Next to his body
	stood a tall Russian soldier, he was holding his gun close to Mykola's face and was using it to
	push his body parts curiously around. Boh had started scanning for any sight of Nikolai, but
	couldn't see him anywhere. Filled with sadness, he felt alone. Boh knew there was no use in
	staying hidden behind the tree just waiting to be found. After a short moment of grief he took up
	the pace and headed for Kaniv, carrying his memory of Mykola deep within.
</p>

