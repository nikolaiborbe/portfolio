<h1 class="text-4xl">A Morning, a Day, and a Night</h1>
<h5>by Nikolai G. Borbe 7th Apr 2022</h5>

<br />

<h2 class="text-2xl">A Morning</h2>

<p>
	The darkness about the place, the creeping cold air, and the ever now and then grunts, could be
	smelt flowing through the air like the ocean on a quiet beach flowing to and fro with the
	moon-tide. A young boy, his family name was not important, nor his history, or legacy, just that
	he went by Boh, laid on an almost empty air mattress. His hair was blond but dark, and was filled
	with mud and dirt, he had bright sky-blue eyes, and was lean of figure; on his left forearm he
	carried an anker tattoo. The mattress he had been using eminently for the last couple of weeks was
	holding vigorously up through the long days. Next to him lay his companion, and only listener,
	Mykola. A bit shorter of stature, but still as handsome as himself. Slowly rising, head bent down,
	he quietly made his way out of the tent. After he left the tent, he made his way over to the
	hillside where they had set up a fireplace and kept their ever-shrinking military ration next to a
	large boulder side; it was Polish dry powder stew, American dry powder stew, and British dry
	powder chili con carne; the bags only required a bit of water to be made into a decent meal. Next
	to the food was the coffee powder which he grabbed and stirred into two wooden cups, creating a
	morning. Boh made his way over to the tent, entered, and carefully shook Mykola till he awaked.
	“Good morning” Mykola grunted, massaging his eyes. “I made you coffee” Boh whispered, reaching his
	hand out giving him the wooden cup. It had been days, maybe even weeks since it was lasted washed;
	it was made of oak but was dark after being (oiled up to keep it from growing old). “Momma’s
	favorite” was engraved on the side, and the handle had broken off, leaving only one way of holding
	the cup. “Thanks, Boh!” He responded, lifting his head up taking a sip. “I had the nightmare again
	Boh, can’t seem to shake it. Every day, I see his face, his young innocent face. I can’t bare it.”
	It took some time for Boh to responded, picking the sentence carefully: “He would have killed you.
	If you didn’t, …, you wouldn’t be here enjoying this coffee with me now.” “I’m growing less and
	less sure he would have Boh, and less and less sure in myself at the same time. He wasn’t a
	killer, he was just a scared little kid, younger than me, I’m sure. Maybe he not shooting first
	makes me… I should have given him the choice to be unsullied, to prove his innocence, but I took
	that choice from him.” Realizing he could do no good Boh responded “Get up, it’s a great day, and
	we have new intel we need to look into”. Both the lads got dressed and headed to the boulder where
	they had set up a living area. They had set up a laptop, a Starlink terminal and some electronics
	into the rock-wall to the left of their ration. With a little roof made out of a few planks and
	logs, a layer of tarp to keep the water guided elsewhere, and to cover it all up, branches from
	fir tree had been used, they had created a fine little workstation. Over on the other side of
	their ration, they kept a well-crafted, little, fireplace. Next to it was a few days work of (cut
	up wood) hid in a woodshed that also was covered in fir tree branches. Everything was made to look
	invisible to anyone that might’ve found their way into the endless depth of the far-reaching
	forest hugging Kiev. “Any updates?” Mykola asked. He was the oldest and had taken on the
	responsibility of contacting all the other groups located around Kiev; twice a day, at 9 am and
	pm, he wrote, updating the Telegram group everything was fine. They were part of an established
	group of civilians tasked with covering the area around Kiev and updating the Ukrainian military
	of enemy position. He was also responsible for the rations. Boh on the other hand was the fixer in
	the duo, he was their engineer, their medic, and relevant for this situation, their IT person. Boh
	had settled down on a stump and was reading updates from their specialized energy-efficient
	computer. They had set up a couple hidden solar panels around the zone to charge the few batteries
	they needed, using the cover of all types of vegetation to their advantage. “Kiev has fallen.” Boh
	looked up, with a concerned expression fixed on his face. “Go and activate the alarms. I will hide
	all of our stuff. Starting tonight, we will do night watches, 4-hour rotations. Remember, our job
	is to get intel when they pass. If we get caught, we are dead.” Mykola Cried. “I’m scared Mykola!”
	Boh answered.
</p>

